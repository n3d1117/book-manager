package it.ned.bookmanager.model;

import java.util.Objects;

public class Author {

    private String id;
    private String name;

    // By default all POJOs must include a public or protected, empty constructor
    // See also http://mongodb.github.io/mongo-java-driver/4.1/bson/pojos/
    protected Author() { }

    public Author(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(final String id) {
        this.id = id;
    }

    public void setName(final String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Author author = (Author) o;
        return id.equals(author.getId()) &&
                Objects.equals(name, author.getName());
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name);
    }

    @Override
    public String toString() {
        return "Author{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                '}';
    }
}
